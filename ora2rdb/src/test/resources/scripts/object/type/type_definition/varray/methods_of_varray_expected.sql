    DECLARE NUMELEMENTS INTEGER;
    DECLARE ELEMENTEXISTS BOOLEAN;
    DECLARE FIRSTELEMENT VARCHAR(100);
    DECLARE LASTELEMENT VARCHAR(100);
    DECLARE LIMITVAR VARCHAR(100);
BEGIN
    /*TEAM := FOURSOME('JOHN', 'MARY', 'ALBERTO', 'JUANITA');*/
